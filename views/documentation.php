<h1><?php echo __('Documentation'); ?></h1>

<h3>Welcome to Notes plugin.</h3>

<p>
    The sole purpose of this plugin was the ability to create, read and delete notes within WolfCMS administration area. 
    But in time (and some feature requests) it is starting to grow in to something else and now you can even show your notes anywhere on your site.
</p>
<p>
    I have created this because I'm toooo lazy to write notes on paper and I needed it for maintenance purposes.
</p>

<h2>Usage</h2>

<p>
    Well, it's pretty simple to use this plugin as it doesn't have to many options. All you can do for the moment is to:
</p>
<ul>
    <li>create new note</li>
    <li>edit and delete existing note</li>
    <li>view all notes</li>
    <li>show note(s) by id on your sites frontend</li>
    <li>show all notes at once on your sites frontend</li>
</ul>
<p>
    For text styling use normal html tags or default Wolf's text filters (Textile or Markdown).
</p>

